// streaming placeholder
function Skeleton({ t, lines = 2 }) {
  return (
    <div style={{ display: "flex", flexDirection: "column", gap: 9 }} aria-hidden="true">
      {Array.from({ length: lines }).map((_, i) => (
        <div key={i} style={{ height: 11, width: i === lines - 1 ? "62%" : "100%", borderRadius: 6, background: t.barBg, opacity: 0.7 }} />
      ))}
    </div>
  );
}
// render content when present; skeleton while streaming; else nothing
const gate = (data, status, render, t, skeletonLines = 3) => {
  if (data) return render();
  if (status === "streaming") return <Skeleton t={t} lines={skeletonLines} />;
  return null;
};

// the per-block territory card: a 3px coloured left edge + the body. bet/handoff
// pass `wash` + bordered=false (open, tinted); handoff also dashedBottom (open door).
