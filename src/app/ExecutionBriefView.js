// in-step modifier callouts. resource -> amber (a constraint); close_call ->
// indigo (a judgement). RENDERED ONLY when text is present (null path draws nothing).
function ClauseBox({ kind, text, t }) {
  const isRes = kind === "resource";
  const accent = isRes ? "rgba(234,179,8,0.38)" : "rgba(127,119,221,0.35)";
  const lead   = isRes ? "#c79a3a" : "#9a93e0";
  const label  = isRes ? "Resource clause" : "Close call";
  const bg     = isRes ? "rgba(234,179,8,0.05)" : "rgba(127,119,221,0.05)";
  return (
    <div style={{ background: bg, borderLeft: `2px solid ${accent}`, borderRadius: "0 8px 8px 0", padding: "9px 13px", marginTop: 9 }}>
      <p style={{ fontSize: 13, lineHeight: 1.5, color: VAL, margin: 0 }}>
        <span style={{ color: lead, fontWeight: 600 }}>{label} — </span>{cap(text)}
      </p>
    </div>
  );
}

