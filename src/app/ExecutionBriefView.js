// ==================================================================== screen
export default function ExecutionBriefView({
  t,
  analysis,
  sections = {},
  status = "idle",
  error = "",
  retrying = false,
  viewingFromSaved = false,
  isBranchIdea = false,
  isReEvalResult = false,
  saveStatus,
  savedIdeaId,
  profile,
  user,
  authLoading,
  getStepNumber,
  setCurrentScreen,
  goToMyIdeas,
  handleLogout,
  onRegenerate,
  onSaveIdea,
  onNewIdea,
  headerStyle,
  footerStyle,
}) {
  const mb = analysis?.estimates?.main_bottleneck;
  const mbColor = mb && mb !== "Specification" ? getMainBottleneckColor(mb, t.mode) : null;

  const nothingYet = !Object.keys(sections || {}).length;
  const fullError = status === "error" && nothingYet; // failed before any block

  // Crossing overlay lifecycle. Show it only when we actually STREAM (didStream),
  // so a seeded/saved brief that mounts already "complete" never flashes it. On
  // complete, hold ~1.2s for the arrival beat ("over to you"), then fade out and
  // reveal the now-sharp brief behind.
  const didStreamRef = useRef(false);
  const [overlay, setOverlay] = useState("hidden"); // "hidden" | "up" | "fading"
  useEffect(() => {
    if (status === "streaming") {
      didStreamRef.current = true;
      setOverlay("up");
      return;
    }
    if (status === "complete") {
      if (!didStreamRef.current) { setOverlay("hidden"); return; }
      const a = setTimeout(() => setOverlay("fading"), 1200);
      const b = setTimeout(() => { setOverlay("hidden"); didStreamRef.current = false; }, 1650);
      return () => { clearTimeout(a); clearTimeout(b); };
    }
    setOverlay("hidden"); // idle / error
  }, [status]);

  // Sticky-crossing scroll progress: 0 at the top of the brief, 1 when the
  // founder note reaches the pinned bar. Computed from the two sentinels and the
  // bar height; if the brief is too short to scroll, it simply starts complete.
  const barRef = useRef(null);
  const topRef = useRef(null);
  const founderRef = useRef(null);
  const [prog, setProg] = useState(0);
  const reduce =
    typeof window !== "undefined" &&
    window.matchMedia &&
    window.matchMedia("(prefers-reduced-motion: reduce)").matches;

  useEffect(() => {
    let raf = 0;
    const calc = () => {
      raf = 0;
      if (!topRef.current || !founderRef.current) return;
      const sy = window.scrollY || document.documentElement.scrollTop || 0;
      const max = (document.documentElement.scrollHeight || 0) - window.innerHeight;
      const barH = barRef.current ? barRef.current.offsetHeight : 72;
      const startY = topRef.current.getBoundingClientRect().top + sy - barH;
      const endY = founderRef.current.getBoundingClientRect().top + sy - barH;
      let p = endY > startY ? (sy - startY) / (endY - startY) : 1;
      if (max <= 4 || sy >= max - 2) p = 1; // too short to scroll, or bottom reached -> complete
      setProg(Math.max(0, Math.min(1, p)));
    };
    const onScroll = () => { if (!raf) raf = requestAnimationFrame(calc); };
    window.addEventListener("scroll", onScroll, { passive: true });
    window.addEventListener("resize", onScroll);
    calc();
    const t1 = setTimeout(calc, 300);
    const t2 = setTimeout(calc, 1200); // re-measure after late layout / fonts
    return () => {
      window.removeEventListener("scroll", onScroll);
      window.removeEventListener("resize", onScroll);
      cancelAnimationFrame(raf);
      clearTimeout(t1); clearTimeout(t2);
    };
  }, [status, sections]);

  return (
    <div style={{ minHeight: "100vh", background: t.bg, color: t.text, display: "flex", flexDirection: "column", overflowX: "clip" }}>
      <header style={headerStyle}>
        <PageContainer wide>
          <div style={{ padding: "16px 0", display: "flex", justifyContent: "space-between", alignItems: "center" }}>
            <h1
              onClick={() => setCurrentScreen(profile && profile.coding && profile.ai ? "input" : "profile")}
              style={{ fontSize: 14, fontFamily: MONO, letterSpacing: "0.1em", textTransform: "uppercase", color: t.mut, margin: 0, cursor: "pointer" }}
            >
              IdeaLoop Core
            </h1>
            <div style={{ display: "flex", alignItems: "center", gap: 12 }}>
              <button
                onClick={() => setCurrentScreen("results2")}
                style={{ fontSize: 12, color: t.mut, background: "none", border: "none", cursor: "pointer" }}
              >
                ← Back to analysis
              </button>
              {!authLoading && user && (
                <>
                  <span style={{ color: t.divider }}>|</span>
                  {!viewingFromSaved && (
                    <>
                      <button onClick={goToMyIdeas} style={{ fontSize: 12, color: t.link, background: "none", border: "none", cursor: "pointer", fontWeight: 500 }}>
                        My Ideas
                      </button>
                      <span style={{ color: t.divider }}>|</span>
                    </>
                  )}
                  <span style={{ fontSize: 12, color: t.mut }}>{user.email}</span>
                  <button onClick={handleLogout} style={{ fontSize: 12, color: t.mut, background: "none", border: "none", cursor: "pointer" }}>
                    Log out
                  </button>
                </>
              )}
            </div>
          </div>
        </PageContainer>
      </header>

      <StepProgress
        currentStep={getStepNumber ? getStepNumber() : 4}
        savedMode={viewingFromSaved}
        branchMode={viewingFromSaved && isBranchIdea}
        t={t}
      />

      <main style={{ flex: 1, paddingBottom: 64 }}>
        <PageContainer wide>
          {/* title */}
          <div style={{ display: "flex", alignItems: "center", gap: 12, marginBottom: 18 }}>
            <div
              style={{
                width: 42,
                height: 42,
                borderRadius: 11,
                background: "rgba(124,92,252,0.13)",
                border: "1px solid rgba(124,92,252,0.3)",
                display: "flex",
                alignItems: "center",
                justifyContent: "center",
                fontSize: 20,
                flexShrink: 0,
              }}
            >
              ➜
            </div>
            <div>
              <p style={{ fontSize: 19, fontWeight: 600, color: t.text, margin: 0 }}>Execution brief</p>
              <p style={{ fontSize: 13, color: t.mut, margin: "2px 0 0" }}>Where our read stops · your handoff</p>
            </div>
            {mb && mbColor && (
              <span
                style={{
                  marginLeft: "auto",
                  display: "inline-flex",
                  alignItems: "center",
                  gap: 6,
                  fontSize: 12,
                  fontWeight: 600,
                  padding: "5px 12px",
                  borderRadius: 9999,
                  background: mbColor.bg,
                  color: mbColor.color,
                  border: `1px solid ${mbColor.border}`,
                  whiteSpace: "nowrap",
                }}
              >
                <MainBottleneckIcon value={mb} size={13} />
                {mb}
              </span>
            )}
          </div>

          {/* soft transient-retry notice */}
          {retrying && status === "streaming" && (
            <div
              style={{
                fontSize: 13,
                color: t.sec,
                background: t.surfAlt,
                border: `1px solid ${t.border}`,
                borderRadius: 10,
                padding: "10px 14px",
                marginBottom: 16,
              }}
            >
              Hit a brief snag generating this — retrying. Thanks for your patience.
            </div>
          )}

          {/* full-failure state (failed before any block streamed) */}
          {fullError ? (
            <Card style={{ padding: 28, textAlign: "center" }} t={t}>
              <p style={{ fontSize: 14, color: t.sec, lineHeight: 1.6, margin: "0 0 16px" }}>
                {error || "We couldn't generate the execution brief just now."}
              </p>
              {onRegenerate && (
                <button
                  onClick={() => onRegenerate()}
                  style={{
                    background: t.ctaBg,
                    color: t.ctaText,
                    border: "none",
                    borderRadius: 10,
                    padding: "11px 26px",
                    fontSize: 13,
                    fontWeight: 600,
                    cursor: "pointer",
                  }}
                >
                  Try again
                </button>
              )}
            </Card>
          ) : (
            <>
              <StickyCrossing prog={prog} reduce={reduce} barRef={barRef} t={t} />
              <div ref={topRef} aria-hidden="true" style={{ height: 0 }} />
              <BetBlock sections={sections} status={status} t={t} />
              <ProveNextBlock sections={sections} status={status} t={t} />
              <OrderBlock sections={sections} status={status} t={t} />
              <WontCountBlock sections={sections} status={status} t={t} />
              <GatesBlock sections={sections} status={status} t={t} />
              <HandoffBlock sections={sections} status={status} t={t} />

              {/* inline error AFTER some content already streamed */}
              {status === "error" && !nothingYet && (
                <p style={{ fontSize: 13, color: t.riskNum, margin: "0 0 22px", lineHeight: 1.5 }}>
                  {error || "The brief stopped before finishing."}
                  {onRegenerate && (
                    <button
                      onClick={() => onRegenerate()}
                      style={{ marginLeft: 10, fontSize: 13, color: t.link, background: "none", border: "none", cursor: "pointer", textDecoration: "underline" }}
                    >
                      try again
                    </button>
                  )}
                </p>
              )}

              <div ref={founderRef} aria-hidden="true" style={{ height: 0 }} />
              <FounderSlot t={t} />

              <BriefFooterActions
                t={t}
                status={status}
                viewingFromSaved={viewingFromSaved}
                isReEvalResult={isReEvalResult}
                saveStatus={saveStatus}
                onSaveIdea={onSaveIdea}
                onNewIdea={onNewIdea}
                goToMyIdeas={goToMyIdeas}
                setCurrentScreen={setCurrentScreen}
                onRegenerate={onRegenerate}
              />
            </>
          )}

          <p style={{ fontSize: 11.5, color: t.mut, textAlign: "center", margin: 0, lineHeight: 1.5 }}>
            IdeaLoop Core — all analysis is AI-generated. Use as a guide, not a definitive assessment.
          </p>
        </PageContainer>
      </main>

      <footer style={footerStyle}>
        <PageContainer wide>
          <p style={{ fontSize: 12, color: t.mut, margin: 0 }}>
            IdeaLoop Core — All analysis is AI-generated. Use as a guide, not a definitive assessment.
          </p>
        </PageContainer>
      </footer>

      {/* Crossing overlay — sharp animation over a blurred backdrop (the brief
          assembles behind it). Mirrors the analyze streaming overlay pattern. */}
      {overlay !== "hidden" && (
        <div
          style={{
            position: "fixed", top: 0, left: 0, right: 0, bottom: 0, zIndex: 9999,
            display: "flex", alignItems: "center", justifyContent: "center",
            backdropFilter: "blur(8px)", WebkitBackdropFilter: "blur(8px)",
            background: t.streamOverlay,
            opacity: overlay === "fading" ? 0 : 1,
            transition: "opacity .4s ease-out",
            animation: overlay === "up" ? "ebOverlayIn .3s ease-out" : "none",
          }}
        >
          <div
            style={{
              background: t.surface,
              border: `1px solid ${t.streamBorder}`,
              borderRadius: 14,
              padding: "36px 28px",
              width: "92%",
              maxWidth: 600,
              boxShadow: t.streamShadow,
              display: "flex",
              justifyContent: "center",
            }}
          >
            <BriefCrossing t={t} status={status} />
          </div>
          <style>{`@keyframes ebBreathe{0%,100%{transform:translate(-50%,-50%) scale(1);opacity:.7}50%{transform:translate(-50%,-50%) scale(1.22);opacity:1}}@keyframes ebOverlayIn{from{opacity:0}to{opacity:1}}`}</style>
        </div>
      )}
    </div>
  );
}