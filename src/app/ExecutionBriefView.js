// Decision gates — stop-loss only. Renders NOTHING when both fields are null
// (honest empty), and no skeleton (gates may legitimately never arrive).
function GatesBlock({ sections, status, t }) {
  const d = sections.gates;
  if (!d) return null;
  const reviseIf = d.revise_if;
  const stopIf = d.stop_if;
  if (!has(reviseIf) && !has(stopIf)) return null;
  return (
    <TerritoryCard edge={PAL.gates.edge} t={t}>
      <SectionHead icon="split" color={PAL.gates.icon} chipBg={PAL.gates.chipBg} chipBorder={PAL.gates.chipBorder} q="Decision gates" t={t} />
      {has(reviseIf) && (
        <Bracket rule={REVISE.rule} labelColor={REVISE.label} label="Revise if">
          <p style={{ fontSize: 14, lineHeight: 1.55, color: VAL2, margin: 0 }}>{cap(reviseIf)}</p>
        </Bracket>
      )}
      {has(stopIf) && (
        <div style={{ marginTop: has(reviseIf) ? 13 : 0 }}>
          <Bracket rule={STOP.rule} labelColor={STOP.label} label="Stop if">
            <p style={{ fontSize: 14, lineHeight: 1.55, color: VAL2, margin: 0 }}>{cap(stopIf)}</p>
          </Bracket>
        </div>
      )}
    </TerritoryCard>
  );
}

